;Labs_44.asm
	ORG 1800H

PSTRING		EQU 0852H
CIN			EQU 08B9H
COUT		EQU 0861H
OUT2X		EQU 0875H
NEWLINE		EQU 088CH
DELAY		EQU 0CE1H
CRLF		EQU 0D0AH
			

START:
		CALL	INIT ; SET UART MARK LEVEL

MAIN:
		CALL	NEWLINE
		LD		IX,HELLO
		CALL	PSTRING
		RET
		JP		MAIN

INIT:
;		LD		A,0FFH
;		CALL	COUT
		CALL	DELAYm
		RET

HELLO:
		DB		'Hello worlds'
		DW		CRLF
		DB		00;
		
DELAYm:
		LD		HL,0000H
Delay1:
		INC		HL
		LD		A,L
		SUB		10H
		LD		A,H
		RLA
		CCF
		RRA
		SBC		A,0A7H
		JR		C,Delay1
		RET


